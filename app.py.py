            df['‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô'] = df['‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô'].astype(str)
            
