            st.dataframe(df_selected.head(10)) # ‡πÇ‡∏ä‡∏ß‡πå‡πÅ‡∏Ñ‡πà 10 ‡πÅ‡∏ñ‡∏ß‡πÅ‡∏£‡∏Å
            
            # 3. ‡∏™‡πà‡∏ß‡∏ô‡πÅ‡∏õ‡∏•‡∏á‡πÄ‡∏õ‡πá‡∏ô CSV ‡πÅ‡∏•‡∏∞‡∏î‡∏≤‡∏ß‡∏ô‡πå‡πÇ‡∏´‡∏•‡∏î
            # ‡πÅ‡∏õ‡∏•‡∏á Dataframe ‡πÄ‡∏õ‡πá‡∏ô CSV ‡πÉ‡∏ô‡∏´‡∏ô‡πà‡∏ß‡∏¢‡∏Ñ‡∏ß‡∏≤‡∏°‡∏à‡∏≥ (Buffer)
            csv_buffer = df_selected.to_csv(index=False).encode('utf-8-sig')
            
